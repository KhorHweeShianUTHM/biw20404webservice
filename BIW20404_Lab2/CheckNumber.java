package BIW20404_Lab2;

public class CheckNumber 
{
    public static void main (String[]args)
    {
        int number = 12;
        
        if(number % 2 == 0)
        {
            System.out.println("This number is an Even number.");
        } else {
            System.out.println("This number is an Odd number.");
        }
    }
}
